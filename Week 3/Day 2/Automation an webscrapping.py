#Automation and webscrapping in python
#Automation is the process of using technology to perform tasks with minimal human intervention. 
# Web scraping is a technique used to extract data from websites. 
# It involves making HTTP requests to a website, retrieving the HTML content, and then parsing that content to extract the desired information.  

