#Variables and Data Types
#variables are used to store data in a program. They are created by assigning a value to a name.
#Data types are the different types of data that can be stored in a variable. Some common data types in Python include:
# - int: integer numbers (e.g., 1, 2, 3)    
# - float: floating-point numbers (e.g., 3.14, 2.718)
# - str: strings (e.g., 'Hello', "World")       
# - bool: boolean values (e.g., True, False)
# - list: ordered, mutable collection of items (e.g., [1, 2, 3])
# - tuple: ordered, immutable collection of items (e.g., (1, 2  , 3))
# - set: unordered collection of unique items (e.g., {1, 2, 3})     
# - dict: collection of key-value pairs (e.g., {'name': 'Alice', 'age': 30})

