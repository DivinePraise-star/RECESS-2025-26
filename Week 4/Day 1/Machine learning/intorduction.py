#machine learning introduction
#What is machine learning?
#Machine learning is a subset of artificial intelligence (AI) that focuses on the development of algorithms and statistical models that enable computers to perform tasks without explicit instructions. 
# Instead, these systems learn from data and improve their performance over time.

#Types of Machine Learning
#1. Supervised Learning
#2. Unsupervised Learning
#3. Reinforcement Learning

# Supervised Learning
# In supervised learning, the model is trained on a labeled dataset, which means that each training example is paired with an output label. 
# The goal is for the model to learn the mapping from inputs to outputs so that it can make predictions on new, unseen data.

# Unsupervised Learning
# In unsupervised learning, the model is trained on an unlabeled dataset, meaning that there are no output labels provided.
#  The goal is to find hidden patterns or intrinsic structures in the input data. 
# Common techniques include clustering and dimensionality reduction.

# Reinforcement Learning
# In reinforcement learning, an agent learns to make decisions by interacting with an environment.  
# The agent receives feedback in the form of rewards or penalties based on its actions, and it aims to maximize the cumulative reward over time.    

